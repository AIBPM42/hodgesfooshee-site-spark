// Helper function to calculate priority based on business rules
export function calculatePriority(confidenceScore: number, equity: number): 'high' | 'medium' | 'low' {
  // High: confidence > 85% AND equity > $100K
  if (confidenceScore > 85 && equity > 100000) {
    return 'high';
  }

  // Medium: confidence > 70% OR equity > $75K
  if (confidenceScore > 70 || equity > 75000) {
    return 'medium';
  }

  // Low: everything else
  return 'low';
}
